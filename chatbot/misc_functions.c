#include <stdio.h>
#include <string.h>
#include <stdlib.h>

void lowercase (char *str)
{
    while (*str)
    {
        if (*string >= 'A' && *string <= 'Z')
        {
            *string = *string + 32;
        }
        string ++;
    }
    
    return;
}

